    public void conectar(){
        SignIn signIn;
        char[] input = textPassword.getPassword();
        usr = textUsuario.getText();
        Log.print("Nuevo intento de acceso con usuario: " + usr);
        signIn = new SignIn(usr, new MD5Hash().makeHash(textPassword.getText()));
        if (signIn.succeded()){
            permiso = (signIn.isAdmin() == 1);
            if(permiso){
                Log.print("Ingresó como usuario con permisos");
                Home.iniciar();
            }else{
                Log.print("Ingresó como usuario sin permisos");
                HomeLite.iniciar();
            }
            Funciones.setSuperUserSession(permiso);
            dispose();
        }else{
            Log.print("No accedio");
            usuarioInvalido();
        }
        
        //Zero print the possible password, for security.
        Arrays.fill(input, '0');
        textPassword.selectAll();
        
        
        
    }
    
    public void usuarioInvalido(){
        JOptionPane.showMessageDialog(
                null, 
                "Usuario inválido o Contraseña incorrecta", 
                "Error", 
                JOptionPane.OK_OPTION,
                null
        );
        Log.print("Usuario inválido o Contraseña incorrecta");
    }
    
    
