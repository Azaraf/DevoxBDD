    private void botonOk2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOk2ActionPerformed
        Date desde = desde2.getDate();
        Date hasta = hasta2.getDate();
        int type = 0;
        if (isText) {
            jFileChooser1.setSelectedFile(new java.io.File("/" + "ReportePrincipal.txt"));
            type = 1;
            isText = false;
        }
        if (isCsv) {
            jFileChooser1.setSelectedFile(new java.io.File("/" + "ReportePrincipal.csv"));
            isCsv = false;
        }
        elegirFecha2.setVisible(false);
        if (jFileChooser1.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            try {
                if (type == 1) {
                    ReporteRMA reporte = new ReporteRMA(desde, hasta);
                    reporte.crear(jFileChooser1.getSelectedFile());
                    type = 0;
                } else {
                    CrearReportePrincipal reporte = new CrearReportePrincipal(desde, hasta);
                    reporte.crear(jFileChooser1.getSelectedFile());
                }

            } catch (IOException ex) {
                if (ex.getMessage().contains("Permission")) {
                    JOptionPane.showMessageDialog(null, "No se puede guardar el archivo en esta ubicación\nporque no tiene permiso. Intente guardarlo en otra ubicación");
                }
                Log.print(ex);
            }
        } else {
            Log.print("No se guardó el Reporte principal");
        }

    }//GEN-LAST:event_botonOk2ActionPerformed

