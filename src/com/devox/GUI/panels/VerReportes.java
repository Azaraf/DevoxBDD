    private void porFechas(Date desde, Date hasta) {
        ReportePorEstadoDeFolio rep = new ReportePorEstadoDeFolio();
        List<List<String>> lista = rep.obtenerCantidadDeFoliosEnEstadoPorFechas(desde.getTime(), hasta.getTime());
        labelSegregacion.setText("0");
        labelDictamen.setText("0");
        labelCaptura.setText("0");
        labelEntregado.setText("0");

        for (List<String> fila : lista) {
            String cant = fila.get(0);
            switch (fila.get(1)) {
                case "Segregación":
                    labelSegregacion.setText(cant);
                    break;

                case "Dictamen":
                    labelDictamen.setText(cant);
                    break;

                case "Captura DLX":
                    labelCaptura.setText(cant);
                    break;

                case "Entregado":
                    labelEntregado.setText(cant);
                    break;
            }
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        jPanel5.setBorder(javax.swing.BorderFactory.createTitledBorder("Estadísticas -del " + sdf.format(desde) + " al " + sdf.format(hasta) + "-"));
    }
    
